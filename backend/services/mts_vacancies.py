def get_all_mts_vacancies():
    """Возвращает все вакансии МТС"""
    return MTS_VACANCIES


def get_vacancy_by_id(vacancy_id: str):
    """Возвращает вакансию по ID"""
    for v in MTS_VACANCIES:
        if v["id"] == vacancy_id:
            return v
    return None


def filter_by_department(department: str):
    """Фильтрует вакансии по департаменту"""
    return [v for v in MTS_VACANCIES if department.lower() in v["department"].lower()]


def filter_by_level(level: str):
    """Фильтрует вакансии по уровню (Intern, Junior, Middle, Senior)"""
    return [v for v in MTS_VACANCIES if level.lower() in v["level"].lower()]


def get_it_vacancies():
    """Возвращает только IT-вакансии"""
    it_keywords = ["ai", "python", "sql", "it", "microsoft", "windows", "сети", "аналитика", "данные", "bi", "ml"]
    result = []
    for v in MTS_VACANCIES:
        tags_lower = [t.lower() for t in v.get("tags", [])]
        if any(kw in tags_lower for kw in it_keywords):
            result.append(v)
    return result
